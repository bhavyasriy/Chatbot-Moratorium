package com.bhavya.model;

public class Loan {
	private String acc_no;
	private String loan_id;
	private String type_of_loan;
	private String loan_amount;
	private String term_period;
	private String amount_paid;
	private String apply_for_moratonium;
	private String settle_loan;
	
	public String getAcc_no() {
		return acc_no;
	}
	public void setAcc_no(String acc_no) {
		this.acc_no = acc_no;
	}
	public String getLoan_id() {
		return loan_id;
	}
	public void setLoan_id(String loan_id) {
		this.loan_id = loan_id;
	}
	public String getType_of_loan() {
		return type_of_loan;
	}
	public void setType_of_loan(String type_of_loan) {
		this.type_of_loan = type_of_loan;
	}
	public String getLoan_amount() {
		return loan_amount;
	}
	public void setLoan_amount(String loan_amount) {
		this.loan_amount = loan_amount;
	}
	public String getTerm_period() {
		return term_period;
	}
	public void setTerm_period(String term_period) {
		this.term_period = term_period;
	}
	public String getAmount_paid() {
		return amount_paid;
	}
	public void setAmount_paid(String amount_paid) {
		this.amount_paid = amount_paid;
	}
	public String getApply_for_moratonium() {
		return apply_for_moratonium;
	}
	public void setApply_for_moratonium(String apply_for_moratonium) {
		this.apply_for_moratonium = apply_for_moratonium;
	}
	public String getSettle_loan() {
		return settle_loan;
	}
	public void setSettle_loan(String settle_loan) {
		this.settle_loan = settle_loan;
	}
	
	

}
