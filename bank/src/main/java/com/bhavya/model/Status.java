package com.bhavya.model;

public class Status{
	private String acc_id;
	private String want;
	private String eco;
	private String health;
	private String mental_health;
	private String extra_loan;
	private String settle;
	private String reason;
	
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	public String getAcc_id() {
		return acc_id;
	}
	public void setAcc_id(String acc_id) {
		this.acc_id = acc_id;
	}
	public String getWant() {
		return want;
	}
	public void setWant(String want) {
		this.want = want;
	}
	public String getEco() {
		return eco;
	}
	public void setEco(String eco) {
		this.eco = eco;
	}
	public String getHealth() {
		return health;
	}
	public void setHealth(String health) {
		this.health = health;
	}
	public String getMental_health() {
		return mental_health;
	}
	public void setMental_health(String mental_health) {
		this.mental_health = mental_health;
	}
	public String getExtra_loan() {
		return extra_loan;
	}
	public void setExtra_loan(String extra_loan) {
		this.extra_loan = extra_loan;
	}
	public String getSettle() {
		return settle;
	}
	public void setSettle(String settle) {
		this.settle = settle;
	}
	
	
		
}